            'endereco' => $request->input('endereco'),
            'telefone' => $request->input('telefone'),
            'cpf' => $request->input('cpf'),
            'email' => $request->input('email'),
            'senha' => $request->input('senha')
        ]);

        $paciente->save();
        
        return redirect()->route('pacientes.index');
    }

